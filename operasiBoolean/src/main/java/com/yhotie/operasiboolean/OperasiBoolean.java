package com.yhotie.operasiboolean;

/**
 *
 * @author usdi.prog
 */
public class OperasiBoolean {

    public static void main(String[] args) {
        /*
        Operator boolean adalah operator yang digunakan untuk melakukan operasi logika antara nilai-nilai boolean (true atau false). Operator-operator ini memungkinkan Anda untuk menggabungkan, membandingkan, dan memanipulasi nilai-nilai boolean untuk mengambil keputusan berdasarkan kondisi tertentu
         */

        // Not
        boolean flag = true;
        System.out.println(!flag);

        boolean a = true;
        boolean b = false;

        // And
        System.out.println(a && b);
        
        // Or
        System.out.println(a || b);
        
        // XOr
        System.out.println(a ^ b);
        
        
        
     

    }
}
